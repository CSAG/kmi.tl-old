App::uses('AppController', 'Controller');

/**
 * Static content controller
 *
 * Override this controller by placing a copy in controllers directory of an application
 *
 * @package       app.Controller
 * @link http://book.cakephp.org/2.0/en/controllers/pages-controller.html
 */
class PagesController extends AppController {

    public $uses = 'Urls';
    public $components = array('RequestHandler');
    public $layout = 'kmi.tl';

    public function index()
    {

    }

    public function ajax_add_url()
    {
        if ($this->request->is('post')){

            $alias = $this->generateRandomString(5);

            $response = array(
                'long' => $this->request->data('url'),
                'short' => Router::url("/${alias}", true),
                'info' => Router::url("/${alias}.info", true)
            );

            $this->set($response);
            $this->set('_serialize', array('long', 'short', 'info'));
        } else {

            throw new NotFoundException();
        }
    }

    private function generateRandomString($length = 10) {
        $characters = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';
        $randomString = '';
        for ($i = 0; $i < $length; $i++) {
            $randomString .= $characters[rand(0, strlen($characters) - 1)];
        }
        return $randomString;
    }

}
